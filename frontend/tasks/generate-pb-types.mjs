#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { runInNewContext } from 'vm';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.join(__dirname, '..', 'src', 'types', 'PBCollections.d.ts');
const relativeOutputPath = outputPath.replace(path.join(__dirname, '..', '..') + '/', '');

// Mock PocketBase API
class FieldList extends Array {
  addAt(index, field) {
    this.splice(index, 0, field);
  }

  removeById(id) {
    const i = this.findIndex(f => f.id === id);
    if (i >= 0) this.splice(i, 1);
  }
}

class Collection {
  constructor(config) {
    Object.assign(this, config);
    this.fields = new FieldList(...(config.fields || []));
  }
}

class Field {
  constructor(config) {
    Object.assign(this, config);
  }
}

const collectionStore = new Map();

const mockApp = {
  findCollectionByNameOrId(nameOrId) {
    return collectionStore.get(nameOrId);
  },
  save(collection) {
    collectionStore.set(collection.id, collection);
    collectionStore.set(collection.name, collection);
    return Promise.resolve();
  },
  delete(collection) {
    collectionStore.delete(collection.id);
    collectionStore.delete(collection.name);
    return Promise.resolve();
  },
  importCollections(collections, _deleteUnused) {
    for (const config of collections) {
      const collection = new Collection(config);
      collectionStore.set(collection.id, collection);
      collectionStore.set(collection.name, collection);
    }
    return Promise.resolve();
  }
};

function mockMigrate(forward, _backward) {
  forward(mockApp);
}

// Snapshot file path is required
const snapshotFile = process.argv[2];
if (!snapshotFile) {
  console.error('Error: A snapshot migration file path is required.');
  console.error('Usage: node tasks/generate-pb-types.mjs <path-to-snapshot-migration.js>');
  console.error('Run via: make collections-sync');
  process.exit(1);
}

// Read and validate snapshot file
let snapshotCode;
try {
  snapshotCode = fs.readFileSync(snapshotFile, 'utf8');
} catch (e) {
  console.error(`Error: Could not read file "${snapshotFile}"`);
  console.error(e.message);
  process.exit(1);
}

if (!snapshotCode.includes('importCollections')) {
  console.error(`Error: "${snapshotFile}" does not appear to be a collections snapshot migration.`);
  console.error('Expected a file containing app.importCollections(...) — generated by: make collections-sync');
  process.exit(1);
}

// Execute the snapshot migration
const context = {
  Collection,
  Field,
  migrate: mockMigrate,
  app: mockApp,
  console,
  process: {
    env: process.env
  }
};
try {
  runInNewContext(snapshotCode, context);
} catch (e) {
  console.error(`Error executing snapshot migration: ${e.message}`);
  process.exit(1);
}

// Field type mapping
function mapFieldType(field) {
  const { type, maxSelect } = field;

  switch (type) {
    case 'text':
    case 'email':
    case 'url':
    case 'editor':
      return 'string';
    case 'number':
      return 'number';
    case 'bool':
      return 'boolean';
    case 'date':
    case 'autodate':
      return 'string';
    case 'json':
      return 'unknown';
    case 'geoPoint':
      return '{ lon: number; lat: number }';
    case 'select':
      if (maxSelect && maxSelect > 1) {
        return 'string[]';
      }
      return 'string';
    case 'relation':
      if (maxSelect && maxSelect > 1) {
        return 'string[]';
      }
      return 'string';
    case 'file':
      if (maxSelect && maxSelect > 1) {
        return 'string[]';
      }
      return 'string';
    default:
      return 'unknown';
  }
}

// Generate TypeScript interface for a collection
function generateInterface(collection) {
  const { name, fields } = collection;

  // Exclude hidden fields (not returned in API responses)
  const customFields = fields.filter(f => !f.hidden);

  const interfaceName = toInterfaceName(name);

  // Format full collection snapshot as JSON comment for AI agents to parse
  // Include all fields and metadata for validation and debugging
  const snapshotJson = JSON.stringify(collection, null, 2);
  const ruleComment = `/**\nsnapshot = ${snapshotJson}\n */`;

  let fieldDefs = customFields
    .map(field => {
      const tsType = mapFieldType(field);
      const optional = !field.required ? '?' : '';
      return `  ${field.name}${optional}: ${tsType};`;
    })
    .join('\n');

  if (fieldDefs) {
    fieldDefs = '\n' + fieldDefs + '\n';
  }

  return `${ruleComment}\nexport interface ${interfaceName} extends PBBaseRecord {${fieldDefs}  collectionName: '${name}';\n}`;
}

// Convert snake_case to PascalCase + Record suffix
function toInterfaceName(collectionName) {
  return collectionName
    .split('_')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join('') + 'Record';
}

// Generate the output file
// Deduplicate collections (they're stored by both id and name)
const seenIds = new Set();
const collections = Array.from(collectionStore.values()).filter(c => {
  if (seenIds.has(c.id)) return false;
  seenIds.add(c.id);
  return true;
});

// Filter to non-system collections (or keep all if needed)
const baseCollections = collections.filter(c => !c.system || c.name.startsWith('_') === false);

const interfaces = baseCollections
  .sort((a, b) => a.name.localeCompare(b.name))
  .map(col => generateInterface(col))
  .join('\n\n');

const collectionRecordsType = baseCollections
  .sort((a, b) => a.name.localeCompare(b.name))
  .map(col => `  ${col.name}: ${toInterfaceName(col.name)};`)
  .join('\n');

const output = `// DO NOT EDIT MANUALLY - Auto-generated by \`make collections-sync\`
// Generated file: ${relativeOutputPath}

/**
 * PocketBase collection metadata present on all records
 */
interface PBBaseRecord {
  collectionId: string;
  collectionName: string;
}

${interfaces}

export type CollectionRecords = {
${collectionRecordsType}
};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output);

console.log(`✓ Generated types for ${baseCollections.length} collections at ${path.relative(process.cwd(), outputPath)}`);
